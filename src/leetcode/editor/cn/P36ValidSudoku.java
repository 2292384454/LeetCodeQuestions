    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public boolean isValidSudoku(char[][] board) {
            //éªŒè¯æ¡ä»¶1
            boolean[] nums;
            for (char[] row : board) {
                nums = new boolean[9];
                for (char c : row) {
                    if (c != '.') {
                        if (nums[c - '1']) {
                            return false;
                        }
                        nums[c - '1'] = true;
                    }
                }
            }
            //éªŒè¯æ¡ä»¶2
            for (int j = 0; j < 9; j++) {
                nums = new boolean[9];
                for (int i = 0; i < 9; i++) {
                    if (board[i][j] != '.') {
                        if (nums[board[i][j] - '1']) {
                            return false;
                        }
                        nums[board[i][j] - '1'] = true;
                    }
                }
            }
            //éªŒè¯æ¡ä»¶3
            for (int k = 0; k < 9; k++) {
                int i_start = (k / 3) * 3, j_start = (k % 3) * 3;
                nums = new boolean[9];
                for (int i = i_start; i < 3 + i_start; i++) {
                    for (int j = j_start; j < 3 + j_start; j++) {
                        if (board[i][j] != '.') {
                            if (nums[board[i][j] - '1']) {
                                return false;
                            }
                            nums[board[i][j] - '1'] = true;
                        }
                    }
                }
            }
            return true;
        }
    }
