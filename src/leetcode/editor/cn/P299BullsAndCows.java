    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public String getHint(String secret, String guess) {
            char[] s = secret.toCharArray(), g = guess.toCharArray();
            int x = 0, y = 0;
            // num[]ä¸­å­˜å‚¨0åˆ°9æ¯ä¸ªæ•°å­—å‡ºçŽ°çš„æ¬¡æ•°ï¼Œåœ¨guessä¸­å‡ºçŽ°å°±+1ï¼Œåœ¨secretä¸­å‡ºçŽ°å°±-1
            // æœ€ç»ˆnumä¸­å­˜å‚¨æ‰€æœ‰çŒœé”™çš„æ•°å­—çŒœé”™çš„æ¬¡æ•°ï¼ˆå½“num[i]>0æ—¶ï¼‰
            int[] num = new int[10];
            for (int i = 0; i < s.length; i++) {
                num[g[i] - '0']++;
                num[s[i] - '0']--;
                x += ((s[i] == g[i]) ? 1 : 0);
            }
            for (int n : num) {
                if (n > 0) {
                    y += n;
                }
            }
            y = s.length - y - x;
            return x + "A" + y + "B";
        }
    }
