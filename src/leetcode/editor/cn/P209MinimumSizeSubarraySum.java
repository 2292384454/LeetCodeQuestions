    class Solution {
        public int minSubArrayLen(int target, int[] nums) {
            int n = nums.length;
            if (n == 0) {
                return 0;
            }
            int res = Integer.MAX_VALUE;
            int left = 0, right = 0, sum = 0;
            while (right < n) {
                sum += nums[right];
                while ((sum - nums[left]) >= target) {
                    sum -= nums[left];
                    left++;
                }
                if (sum >= target) {
                    res = Math.min(res, right - left + 1);
                }
                right++;
            }
            return res == Integer.MAX_VALUE ? 0 : res;
        }
    }
